import math

num = 20
nums = [11, 3, 2, 6, 17]

print("Square root of the number : ", math.sqrt(num))

print("Number to the power : ", math.pow(num, 3))

print("Minimum number in the list : ", min(nums))

print("Maximum number in the list : ", max(nums))

neg_num = -908.248
print("Absolute value : ", math.fabs(neg_num))

decimal_num = 18.7654
print("Floor : ", math.floor(decimal_num))

print("Ceil : ", math.ceil(decimal_num))

print("Truncate : ", math.trunc(decimal_num))
