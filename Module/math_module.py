import math

num = 20
nums = [11, 3, 2, 6, 17]

print("Square root of the number : ", math.sqrt(num)) # Square root of the number :  4.47213595499958

print("Number to the power : ", math.pow(num, 3))     # Number to the power :  8000.0

print("Minimum number in the list : ", min(nums))     # Minimum number in the list :  2

print("Maximum number in the list : ", max(nums))     # Maximum number in the list :  17

neg_num = -908.248
print("Absolute value : ", math.fabs(neg_num))        # Absolute value :  908.248

decimal_num = 18.7654
print("Floor : ", math.floor(decimal_num))            # Floor :  18

print("Ceil : ", math.ceil(decimal_num))              # Ceil :  19

print("Truncate : ", math.trunc(decimal_num))         # Truncate :  18
